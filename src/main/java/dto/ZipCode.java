package dto;

public class ZipCode {
	private String zip_num;
	private String sido;
	private String gugun;
	private String dong;
	private String zip_code;
	private String bunji;
	
	
	public String getZip_num() {
		return zip_num;
	}
	public ZipCode setZip_num(String zip_num) {
		this.zip_num = zip_num;
		return this;
	}
	public String getSido() {
		return sido;
	}
	public ZipCode setSido(String sido) {
		this.sido = sido;
		return this;
	}
	public String getGugun() {
		return gugun;
	}
	public ZipCode setGugun(String gugun) {
		this.gugun = gugun;
		return this;
	}
	public String getDong() {
		return dong;
	}
	public ZipCode setDong(String dong) {
		this.dong = dong;
		return this;
	}
	public String getZip_code() {
		return zip_code;
	}
	public ZipCode setZip_code(String zip_code) {
		this.zip_code = zip_code;
		return this;
	}
	public String getBunji() {
		return bunji;
	}
	public ZipCode setBunji(String bunji) {
		this.bunji = bunji;
		return this;
	}

}
